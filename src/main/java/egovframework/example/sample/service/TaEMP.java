package egovframework.example.sample.service;

public class TaEMP {

	private int taEmpCode;
	private String taEname;
	private String taJob;
	private String taPhoneNum;
	private String taBirthDate;
	private String taHireDate;
	private int taDeptCode;
	
	public int getTaEmpCode() {
		return taEmpCode;
	}
	public void setTaEmpCode(int taEmpCode) {
		this.taEmpCode = taEmpCode;
	}
	public String getTaEname() {
		return taEname;
	}
	public void setTaEname(String taEname) {
		this.taEname = taEname;
	}
	public String getTaJob() {
		return taJob;
	}
	public void setTaJob(String taJob) {
		this.taJob = taJob;
	}
	public String getTaPhoneNum() {
		return taPhoneNum;
	}
	public void setTaPhoneNum(String taPhoneNum) {
		this.taPhoneNum = taPhoneNum;
	}
	public String getTaBirthDate() {
		return taBirthDate;
	}
	public void setTaBirthDate(String taBirthDate) {
		this.taBirthDate = taBirthDate;
	}
	public String getTaHireDate() {
		return taHireDate;
	}
	public void setTaHireDate(String taHireDate) {
		this.taHireDate = taHireDate;
	}
	public int getTaDeptCode() {
		return taDeptCode;
	}
	public void setTaDeptCode(int taDeptCode) {
		this.taDeptCode = taDeptCode;
	}
	
	
	
}
